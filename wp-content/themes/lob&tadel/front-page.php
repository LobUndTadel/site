                  <h2>Über Lob &amp; Tadel</h2>
                  <p>An unserer Hochschule entstehen jedes Semester Arbeiten und Produktionen, die sich in ihrer Form, Kreativität und ihrer Rezeption unterscheiden. Die MediaNight bietet eine Möglichkeit der Präsentation und Ehrung. Jedoch kann immer nur ein Bruchteil dessen, was fotografiert, getextet, komponiert, gefilmt, gestaltet oder programmiert wurde, ausgestellt werden. Nicht jede studentische Produktion entspricht dem Format des Semesterabschlusses unserer Fakultät. Ebenso entstehen nicht alle Werke in Veranstaltungen, die sich präsentieren lassen.</p>

                  <p>Die Möglichkeit all diese Arbeiten jedes Einzelnen auf einfachem Wege zu sammeln, zu teilen und auch zu kommunizieren, könnte dem Wesen der Fakultät einen weiteren Schub verleihen. Die Formatgrenzen und Semesterherkunft werden fallen gelassen - der aktive und freie Austausch wird gefördert. Inspiration durch und für andere von Uns. Das kann ich - das will ich zeigen, weil...! Wie denkt Ihr über meine Idee?</p>

